/*
    Les opérateurs arithmétiques
*/

let nb1 = 10, nb2 = 65, resultat

// L'addition
resultat = nb1 + nb2
console.log(resultat);
console.log(`${nb1} + ${nb2} = ${resultat}`);

// La soustraction
resultat = nb1 - nb2
console.log(`${nb1} - ${nb2} = ${resultat}`);

// Les multiplications
resultat = nb1 * nb2
console.log(`${nb1} x ${nb2} = ${resultat}`);

// Division
resultat = nb1 / nb2
console.log(`${nb1} / ${nb2} = ${resultat}`);

// Modulo (reste de la division)
resultat = nb1 % nb2
console.log(`${nb1} % ${nb2} = ${resultat}`);