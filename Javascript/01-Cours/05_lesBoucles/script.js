/*
    Les boucles
*/

// Boucle Tant que ... (while)

let i = 1

while (i <= 10) {
    console.log(`La boucle while s'est exécutée ${i} fois`);
    i++
}

// Boucle Faire tant que ... (do while)

let n = 1

do{
    console.log(`La boucle do while s'est exécutée ${i} fois`);
    n++
} while (n <= 10)

// boucle for

for (let k = 0; k < 10; k++){
    console.log(`La boucle for s'est exécutée ${i} fois`);
}